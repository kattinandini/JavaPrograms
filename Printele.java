public class Printele {
    public static void main(String[] args) {
        // Initializing an array
        int[] arr = {1, 2, 3, 4, 5};

        // Printing array elements
        System.out.println("Array elements are:");
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}
